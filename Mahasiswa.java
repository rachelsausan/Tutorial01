
public class Mahasiswa {
	private String name;
    private String npm;
    
    // ini method setter
    public void setName(String name){
    	
    	this.name = name;
    	
    }
    
    public void setNpm(String npm){
    	this.npm = npm;
	}
    
    // ini method getter
    public String getName(){
    	return this.name;
    }
    
    public String getNpm(){
    	return this.npm;
	}
	

}
